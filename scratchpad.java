public class scratchpad {


	public static void main(String[] args) {
		int a = 5, b = 11;
		System.out.println(b + a); // Prints
		System.out.println(b - a); // Prints
		System.out.println(b * a); // Prints
		System.out.println(b / a); // Prints
		System.out.println(b % a); // Prints
		System.out.println((a - b) + a * (b - a) - a % b); // Prints
	}



}
